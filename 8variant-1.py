перший_рядок = input("Введіть перший рядок символів: ")
другий_рядок = input("Введіть другий рядок символів: ")

перший_рядок_символи = set(перший_рядок)
другий_рядок_символи = set(другий_рядок)

різниця = перший_рядок_символи - другий_рядок_символи

print("Символи, які є у першому рядку і відсутні у другому рядку:")
for символ in різниця:
    print(символ)
